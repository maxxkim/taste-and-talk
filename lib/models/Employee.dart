class Employee {
  String docId;
  String firstName;
  String lastName;
  String position;
  String dateOfBirth;
  String avatarURL;
  String email;

  Employee({
    required this.docId,
    required this.firstName,
    required this.lastName,
    required this.position,
    required this.dateOfBirth,
    required this.avatarURL,
    required this.email,
  });
}
